#include "CameraGame.h"

//-----------------------------------------------------------------------------
// コンストラクタ
//-----------------------------------------------------------------------------
CCameraGame::CCameraGame()
{
}

//-----------------------------------------------------------------------------
// デストラクタ
//-----------------------------------------------------------------------------
CCameraGame::~CCameraGame()
{
}

//-----------------------------------------------------------------------------
// 初期化
//-----------------------------------------------------------------------------
void CCameraGame::Init()
{
	CCamera::Init();
	//視点・注視点・上方向を設定
	m_posV = D3DXVECTOR3(0.0f, 410.0f, -355.0f);
}

//-----------------------------------------------------------------------------
// 更新
//-----------------------------------------------------------------------------
void CCameraGame::Update()
{
	CCamera::Update();
}

//-----------------------------------------------------------------------------
// 生成
//-----------------------------------------------------------------------------
CCameraGame* CCameraGame::Create()
{
	CCameraGame* pCamera = new CCameraGame;

	if (pCamera == nullptr)
	{
		return nullptr;
	}

	pCamera->Init();
	return pCamera;
}
