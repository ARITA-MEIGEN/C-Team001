//=============================================================================
// 生成
//=============================================================================
CBlock* CBlock::Create(D3DXVECTOR3 pos, float lot)
{
	CBlock*pBlock;
	pBlock = new CBlock(5);
	if (pBlock != nullptr)
	{// ポリゴンの初期化処理
		pBlock->SetModel("data/MODEL/box.x");
		pBlock->Init();
		pBlock->SetPos(pos);
		pBlock->SetRot(D3DXVECTOR3(0.0f,0.0f,0.0f));
	}
	return pBlock;
}

//=============================================================================
// プレイヤー設定
//=============================================================================
void CBlock::SetPlayerNumber(int number)
{
	m_number = number;
	switch (m_number)
	{
	case 0:
		SetCol(D3DXCOLOR(1.0f, 0.0f, 0.0f, 1.0f));
		break;
	case 1:
		SetCol(D3DXCOLOR(0.0f, 0.0f, 1.0f, 1.0f));
		break;
	case 2:
		SetCol(D3DXCOLOR(1.0f, 1.0f, 0.0f, 1.0f));
		break;
	case 3:
		SetCol(D3DXCOLOR(0.0f, 1.0f, 0.0f, 1.0f));
		break;
	default:
		break;
	}

}
